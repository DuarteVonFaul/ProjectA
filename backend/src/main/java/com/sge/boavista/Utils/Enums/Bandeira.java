    private final int codigo;

    Bandeira(int codigo) {
        this.codigo = codigo;
    }

    public int getCodigo() {
        return codigo;
    }

    public static Bandeira getByCodigo(int codigo) {
        return Arrays.stream(values())
                .filter(bandeira -> bandeira.getCodigo() == codigo)
                .findFirst()
                .orElse(null);
    }

    /**
     * Busca o código de uma bandeira pelo seu nome.
     * A busca não diferencia maiúsculas de minúsculas.
     *
     * @param nome O nome da bandeira a ser buscada.
     * @return O código da bandeira.
     * @throws IllegalArgumentException se nenhuma bandeira com o nome especificado for encontrada.
     */
    public static int getCodigoPeloNome(String nome) {
        try{
            return Arrays.stream(values())
                    .filter(bandeira -> bandeira.name().equalsIgnoreCase(nome))
                    .findFirst()
                    .map(Bandeira::getCodigo)
                    .orElseThrow(() -> new IllegalArgumentException("Nenhuma bandeira encontrada com o nome: " + nome));
        }catch (Exception e) {
            return 0;
        }
    }
